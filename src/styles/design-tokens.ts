// Utility function to get design token values
export const getToken = (path: string) => {
  const keys = path.split('.');
  let value: any = designTokens;
  
  for (const key of keys) {
    value = value[key];
    if (value === undefined) {
      console.warn(`Design token not found: ${path}`);
      return undefined;
    }
  }
  
  return value;
};

// Type definitions for design tokens
export type DesignTokens = typeof designTokens;
export type ColorToken = keyof typeof designTokens.colors;
export type SpacingToken = keyof typeof designTokens.spacing;
export type TypographyToken = keyof typeof designTokens.typography; 