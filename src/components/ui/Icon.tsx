export const Icon: React.FC<IconProps> = ({
  name,
  size = 'md',
  className,
  color
}) => {
  const IconComponent = icons[name as keyof typeof icons];
  
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }
  
  return (
    <IconComponent
      className={cn(
        sizeClasses[size],
        'inline-block',
        className
      )}
      style={color ? { color } : undefined}
      aria-hidden="true"
    />
  );
};

